/**
 * Supported file types for the Office SDK.
 */
export enum FileType {
  Document = 'document', // Document file, like .doc, .docx
  Spreadsheet = 'spreadsheet', // Spreadsheet file, like .xls, .xlsx
  Presentation = 'presentation', // Presentation file, like .ppt, .pptx
  LiteDoc = 'ltdoc', // Lite document.
  DBTable = 'dbtable', // Database table.
  Pdf = 'pdf', // PDF file.
}
